async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет! Я бот-викторина по творчеству Виктора Астафьева.\n"
        "Выбери тему, по которой хочешь проверить свои знания:",
        reply_markup=get_topics_keyboard()
    )
    return TOPIC

async def topic_selected(update: Update, context: ContextTypes.DEFAULT_TYPE):
    topic = update.message.text
    if topic not in QUESTIONS:
        await update.message.reply_text("Пожалуйста, выберите тему из списка.", reply_markup=get_topics_keyboard())
        return TOPIC

    easy_qs = QUESTIONS[topic]["легкий"]
    medium_qs = QUESTIONS[topic]["средний"]
    hard_qs = QUESTIONS[topic]["сложный"]

    random.shuffle(easy_qs)
    random.shuffle(medium_qs)
    random.shuffle(hard_qs)

    questions = easy_qs + medium_qs + hard_qs
    stage_boundaries = [len(easy_qs), len(easy_qs) + len(medium_qs)]

    context.user_data['topic'] = topic
    context.user_data['questions'] = questions
    context.user_data['current_index'] = 0
    context.user_data['correct_count'] = 0
    context.user_data['total_questions'] = len(questions)
    context.user_data['stage_boundaries'] = stage_boundaries
    context.user_data['current_stage'] = 0
    context.user_data['total_stages'] = 3

    await send_question(update, context)
    return QUIZ

async def send_question(update: Update, context: ContextTypes.DEFAULT_TYPE):
    index = context.user_data['current_index']
    questions = context.user_data['questions']
    if index >= len(questions):
        await finish_quiz(update, context)
        return

    q_data = questions[index]
    question_text = q_data['question']
    options = q_data['options']
    context.user_data['correct_answer'] = options[q_data['correct']]

    keyboard = get_options_keyboard(options)
    await update.message.reply_text(
        f"Вопрос {index+1} из {len(questions)}:\n{question_text}",
        reply_markup=keyboard
    )

async def answer_received(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_answer = update.message.text
    correct_answer = context.user_data.get('correct_answer')
    if not correct_answer:
        await update.message.reply_text("Что-то пошло не так. Начните заново /start")
        return ConversationHandler.END

    if user_answer == correct_answer:
        context.user_data['correct_count'] += 1
        await update.message.reply_text("✅ Правильно! Молодец!")
    else:
        await update.message.reply_text(f"❌ Неправильно. Правильный ответ: {correct_answer}")

    context.user_data['current_index'] += 1
    next_index = context.user_data['current_index']
    questions = context.user_data['questions']
    stage_boundaries = context.user_data['stage_boundaries']

    if next_index >= len(questions):
        await finish_quiz(update, context)
        return

    current_stage = context.user_data.get('current_stage', 0)
    new_stage = 0
    for i, boundary in enumerate(stage_boundaries):
        if next_index > boundary:
            new_stage = i + 1
        else:
            break

    if new_stage > current_stage:
        context.user_data['current_stage'] = new_stage
        correct = context.user_data['correct_count']
        answered = next_index
        await update.message.reply_text(
            f"Вы завершили этап! На данный момент вы ответили правильно на {correct} из {answered} вопросов.\n"
            f"Хотите перейти на следующий уровень?",
            reply_markup=get_yes_no_keyboard()
        )
        context.user_data['waiting_for_next_level'] = True
        return ASK_NEXT
    else:
        await send_question(update, context)

async def next_level_decision(update: Update, context: ContextTypes.DEFAULT_TYPE):
    decision = update.message.text
    if decision not in ["Да", "Нет"]:
        await update.message.reply_text("Пожалуйста, ответьте 'Да' или 'Нет'.", reply_markup=get_yes_no_keyboard())
        return ASK_NEXT

    if decision == "Да":
        context.user_data['waiting_for_next_level'] = False
        await send_question(update, context)
        return QUIZ
    else:
        await finish_quiz(update, context, early_exit=True)
        return TOPIC

async def finish_quiz(update: Update, context: ContextTypes.DEFAULT_TYPE, early_exit=False):
    correct = context.user_data.get('correct_count', 0)
    total = context.user_data.get('total_questions', 0)
    if early_exit:
        await update.message.reply_text(
            f"Вы завершили викторину досрочно.\n"
            f"Ваш результат: {correct} из {total} правильных ответов.\n"
            "Возвращаемся в главное меню.",
            reply_markup=get_topics_keyboard()
        )
    else:
        await update.message.reply_text(
            f"🎉 Викторина окончена!\n"
            f"Ваш результат: {correct} из {total} правильных ответов.\n"
            "Вы можете выбрать новую тему.",
            reply_markup=get_topics_keyboard()
        )
    context.user_data.clear()
    return TOPIC

async def cancel(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("Викторина отменена. Начните с /start", reply_markup=ReplyKeyboardRemove())
    context.user_data.clear()
    return ConversationHandler.END

