# ФУНКЦИЯ ЗАПУСКА БОТА (в главном потоке)
# ------------------------------------------------------------
def run_bot():
    application = ApplicationBuilder().token(BOT_TOKEN).build()

    conv_handler = ConversationHandler(
        entry_points=[CommandHandler('start', start)],
        states={
            TOPIC: [MessageHandler(filters.TEXT & ~filters.COMMAND, topic_selected)],
            QUIZ: [MessageHandler(filters.TEXT & ~filters.COMMAND, answer_received)],
            ASK_NEXT: [MessageHandler(filters.TEXT & ~filters.COMMAND, next_level_decision)],
        },
        fallbacks=[CommandHandler('cancel', cancel)],
    )

    application.add_handler(conv_handler)
    # Главное изменение: отключаем обработку сигналов
    application.run_polling(stop_signals=None)

