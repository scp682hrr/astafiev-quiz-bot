def run_flask():
    """Запускает Flask-сервер для health check в отдельном потоке."""
    port = int(os.environ.get("PORT", 8080))
    logging.info(f"Запуск веб-сервера на порту {port}...")
    app.run(host='0.0.0.0', port=port)

